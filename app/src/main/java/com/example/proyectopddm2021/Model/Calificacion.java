package com.example.proyectopddm2021.Model;

public class Calificacion {
    String idTurista;
    String idLugarTuristico;
    String CantidadEstrella;

    public String getIdTurista() {
        return idTurista;
    }

    public void setIdTurista(String idTurista) {
        this.idTurista = idTurista;
    }

    public String getIdLugarTuristico() {
        return idLugarTuristico;
    }

    public void setIdLugarTuristico(String idLugarTuristico) {
        this.idLugarTuristico = idLugarTuristico;
    }

    public String getCantidadEstrella() {
        return CantidadEstrella;
    }

    public void setCantidadEstrella(String cantidadEstrella) {
        CantidadEstrella = cantidadEstrella;
    }
}
